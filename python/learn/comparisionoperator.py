""" 
   ==  equals to 
   !=  not equals to
   >= greater than equals
   <= less than equals
"""

print(3 == 3)
#output is true
print(3 != 3)
#output is false
print(7 >= 2)
#output is true
print(10 <= 2)
#output is false



